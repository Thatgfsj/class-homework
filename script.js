function renderHomeworks(homeworks) {
  if (homeworks.length === 0) {
    elements.homeworkGrid.innerHTML = '';
    elements.emptyState.style.display = 'block';
    return;
  }
  elements.emptyState.style.display = 'none';
  elements.homeworkGrid.innerHTML = homeworks.map((hw, index) => createHomeworkCard(hw, index)).join('');
  bindCardEvents();
}

function renderModal(homework) {
  const subjectStyle = subjectColors[homework.subject] || subjectColors["其他"];
  const countdown = getCountdownText(getDaysUntilDue(homework.dueDate));
  const isExpired = homework.status === 'expired' || getDaysUntilDue(homework.dueDate) < 0;
  const hasAIPrompt = homework.description.includes('AI 参考提示词') || homework.description.includes('# 角色设定');
  elements.modal.style.setProperty('--subject-color', subjectStyle.color);
  elements.modal.style.setProperty('--subject-bg', subjectStyle.bg);
  const parts = homework.description.split('AI 参考提示词');
  let descHTML = '';
  if (parts.length > 1) {
    const intro = parts[0].split('\n').map(p => '<p>' + p + '</p>').join('');
    const copyBtn = '<button class="copy-prompt-btn" onclick="copyAIPrompt()">复制提示词</button>';
    const promptText = parts[1].replace(/^[:：]\n?/, '').trim();
    descHTML = intro + '<div class="ai-prompt-section">' + copyBtn + '<pre class="ai-prompt-text">' + promptText + '</pre></div>';
  } else {
    descHTML = homework.description.split('\n').map(p => '<p>' + p + '</p>').join('');
  }
  elements.modalContent.innerHTML = `
    <div class="modal-header">
      <span class="modal-subject" style="--subject-color: ${subjectStyle.color}; --subject-bg: ${subjectStyle.bg};">
        ${homework.subject}
      </span>
      <h2 class="modal-title">${homework.title}</h2>
      <div class="modal-meta">
        <span class="modal-date">截止：${formatFullDate(homework.dueDate)} · ${getWeekday(homework.dueDate)}</span>
        ${!isExpired ? '<span class="modal-countdown ' + (countdown.urgent ? 'urgent' : '') + '">' + countdown.value + ' ' + countdown.text + '</span>' : '<span class="modal-countdown expired">已截止</span>'}
      </div>
    </div>
    <div class="modal-body">
      ${descHTML}
    </div>
  `;
}

