function bindCardEvents() {
  document.querySelectorAll('.homework-card').forEach(card => {
    card.addEventListener('click', () => {
      const id = card.dataset.id;
      const homework = homeworkData.homeworks.find(h => h.id === id);
      if (homework) {
        renderModal(homework);
        elements.modalOverlay.classList.add('visible');
        document.body.style.overflow = 'hidden';
      }
    });
  });
}

function copyAIPrompt() {
  const pre = document.querySelector('.ai-prompt-text');
  if (pre) {
    navigator.clipboard.writeText(pre.textContent).then(() => {
      showToast('已复制到剪贴板');
    }).catch(() => {
      showToast('复制失败，请手动选择复制');
    });
  }
}

function showToast(message) {
  let toast = document.getElementById('toast');
  if (!toast) {
    toast = document.createElement('div');
    toast.id = 'toast';
    toast.style.cssText = 'position:fixed;bottom:20px;left:50%;transform:translateX(-50%);background:#333;color:#fff;padding:10px 20px;border-radius:6px;font-size:14px;z-index:10000;opacity:0;transition:opacity 0.3s';
    document.body.appendChild(toast);
  }
  toast.textContent = message;
  toast.style.opacity = '1';
  setTimeout(() => { toast.style.opacity = '0'; }, 2000);
}

function closeModal() {
  elements.modalOverlay.classList.remove('visible');
  document.body.style.overflow = '';
}

function filterHomeworks(filter = 'all', searchTerm = '') {
  let filtered = homeworkData.homeworks;
  if (filter !== 'all') {
    filtered = filtered.filter(h => h.subject === filter);
  }
  if (searchTerm) {
    const term = searchTerm.toLowerCase();
    filtered = filtered.filter(h =>
      h.title.toLowerCase().includes(term) ||
      h.description.toLowerCase().includes(term) ||
      h.subject.toLowerCase().includes(term)
    );
  }
  const now = new Date();
  const active = filtered.filter(h => {
    const due = new Date(h.dueDate + 'T23:59:59');
    return h.status === 'active' && due >= now;
  });
  if (active.length > 0) {
    renderHomeworks(active);
    elements.showExpiredBtn.style.display = 'block';
    elements.loadingState.classList.remove('visible');
  } else if (filtered.length > 0) {
    renderHomeworks([]);
    elements.emptyState.querySelector('.empty-title').textContent = '暂无进行中作业';
    elements.emptyState.querySelector('.empty-text').textContent = '所有作业都已截止或已筛选掉';
    elements.showExpiredBtn.style.display = 'block';
    elements.loadingState.classList.remove('visible');
  }
}

